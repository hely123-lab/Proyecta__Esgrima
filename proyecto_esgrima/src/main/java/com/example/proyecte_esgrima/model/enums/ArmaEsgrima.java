package com.example.proyecte_esgrima.model.enums;

/**
 * Enumeración que representa los distintos tipos de armas utilizados en el
 * deporte de la esgrima.
 * 
 */
public enum ArmaEsgrima {

	/**
	 * Espada: Modalidad en la que todo el cuerpo es zona válida de toque.
	 */
	ESPADA,

	/**
	 * Florete: Solo el torso es zona válida de toque.
	 */
	FLORETE,

	/**
	 * Sable: La zona válida incluye torso, brazos y cabeza. Se puede tocar con filo
	 * y punta.
	 */
	SABLE
}
